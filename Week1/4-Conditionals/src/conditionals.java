
public class conditionals {

	public static void main(String[] args) {
		int no = 21;
		
		if(no<20) {
			System.out.println(no + " is less than 20. ");
		}else if(no == 20) {
			System.out.println(no + " is equal to 20");
		}
		else {
			System.out.println(no + " is higher than 20");
		}
	}

}
